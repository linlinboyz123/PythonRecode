# Function的中文叫函數

# Python的常見方法OR叫函數?

import math  # 引入數學函數庫
print(abs(-5))  # 絕對值
print(pow(2, 10))  # 函數的次方寫法
print(max(4, 7, 9, 5))  # 找出最大的數
print(min(4, 7, 9, 5))  # 找出最小的數
print(round(2.5))
# 四捨五入，有個特別的規則，如果是x.5的情況下，進位後的樹如果是基數，他就不會進位，例如2.5進位是3，那他就不會進位，因此答案還是2
print(int(3.0))  # 浮點換整數
print(str(1))  # 整數轉字串
print(float(3))  # 浮點轉整數

print(math.e)  # 2.71
print(math.pi)  # 3.14
print(math.floor(4.924636))  # 無條件捨棄
print(math.ceil(4.243728))  # 無條件進位
print(math.sqrt(36))  # 開根號
